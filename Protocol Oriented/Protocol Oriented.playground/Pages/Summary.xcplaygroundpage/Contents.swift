//: # Summary
//:
//: We check the basic concepts of Protocol Oriented Programming. Some important points to keep in mind:
//: * Start defining you protocols
//: * You can inherit from and implement multiple protocols! Define fine-grained protocols
//: * Use value-types whenever possible
//: * Choose wisely when use classes and inheritance like when extending from external classes or when recursive references are needed.

